// ─── streamed events from app-server ─────────────────────────────────
//
// app-server emits notifications (no id, has method) on long-running
// turns. We forward these to the chat UI. The full upstream catalogue
// is wider; we only branch on the subset we render today.

export type AppServerNotification =
  | { method: "turn/itemAdded";       params: { turnId: string; item: TurnItem } }
  | { method: "turn/itemUpdated";     params: { turnId: string; item: TurnItem } }
  | { method: "turn/finished";        params: { turnId: string; reason?: string } }
  | { method: "tool/call";            params: { turnId: string; toolName: string; arguments: any; callId: string } }
  | { method: "tool/result";          params: { turnId: string; callId: string; output: string; error?: string } }
  | { method: "command/executionRequested";
      params: { turnId: string; commandId: string; command: string; cwd?: string } }
  | { method: "file/changeRequested";
      params: { turnId: string; changeId: string; path: string; diff: string } }
  | { method: "approval/required";
      params: ApprovalRequest }
  | { method: "log";                  params: { level: "info" | "warn" | "error"; message: string } };

export interface TurnItem {
  type: "text" | "tool_use" | "tool_result" | "thinking";
  content?: string;
  // Free-form for forward-compat.
  [k: string]: any;
}

// ─── approval ───────────────────────────────────────────────────────
//
// P64.2 — Real codex emits approvals as SERVER-INITIATED REQUESTS
// (one of eight method names on ServerRequest.ts in 0.130.0). They
// carry an `id` and we MUST send a JSON-RPC response back so the turn
// can proceed. Earlier iterations modelled a "approval/respond" client
// method — that does not exist in the protocol.
//
// AppServerClient.installApprovalBridge() projects each server-
// initiated approval method onto a synthesized "approval/required"
// notification with the legacy `ApprovalRequest` shape below, so the
// existing chat-bridge UX continues to work. approvalRespond() resolves
// the right pending JSON-RPC id under the hood.

export type ApprovalKind = "command" | "file" | "network" | "tool";

export interface ApprovalRequest {
  approvalId: string;
  turnId: string;
  kind: ApprovalKind;
  // Human-readable summary the UI shows above the choice buttons.
  summary: string;
  // Optional detail the UI may render in a collapsible section.
  detail?: string;
  // For command approvals: the command string + cwd. For file: path + diff.
  command?: string;
  cwd?: string;
  path?: string;
  diff?: string;
}

export type ApprovalDecision =
  | "accept"
  | "acceptForSession"
  | "decline"
  | "cancel";

// Kept as a type alias only — the concrete dispatch path is now the
// approval bridge in AppServerClient (see installApprovalBridge).
export interface ApprovalRespondParams {
  approvalId: string;
  decision: ApprovalDecision;
}

// ─── DB row for the bridge connection ────────────────────────────────
//
// Phase 1 manual bridge has TWO sub-variants we need to track explicitly,
// because they have different reachability + safety properties:
//
//   "browser"      The user's browser tab is the client. Bridge runs
//                  on the same machine as the browser. URL is
//                  loopback / RFC1918 / *.local. Hosted Vercel server
//                  CANNOT reach this URL — only the browser can. We
//                  refuse server-side fetches when this is set.
//
//   "tunnel"       The bridge sits behind a public tunnel (ngrok,
//                  Cloudflare Tunnel, a self-hosted reverse proxy).
//                  URL is wss:// against a public DNS / IP. Hosted
//                  server can reach it AFTER passing the SSRF
//                  validator + connection-time DNS guard.
//
//   "local-server" Our Node runtime IS the user's machine (npm run
//                  dev / desktop wrapper). Server-side fetches to
//                  loopback are legitimate because it's the same host.
//                  Refused on Vercel.
//
// We default to "browser" for any new config that doesn't specify; old
// rows from P57 are inferred via inferConnectionLocationFromUrl().
export type CodexBridgeLocation = "browser" | "tunnel" | "local-server";

export interface CodexBridgeConfig {
  url: string;
  capabilityToken: string;
  experimentalApi?: boolean;
  connectionLocation?: CodexBridgeLocation;
}
